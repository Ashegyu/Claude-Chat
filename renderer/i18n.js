  let currentLang = localStorage.getItem('uiLanguage') || 'ko';

  function t(key, ...args) {
    const lang = LANGS[currentLang] || LANGS.ko;
    let str = lang[key] || LANGS.ko[key] || key;
    // Simple placeholder replacement: {0}, {1}, ...
    for (let i = 0; i < args.length; i++) {
      str = str.replace(new RegExp('\\{' + i + '\\}', 'g'), args[i]);
    }
    return str;
  }

  function setLanguage(lang) {
    if (!LANGS[lang]) return;
    currentLang = lang;
    localStorage.setItem('uiLanguage', lang);
  }

  function getLanguage() {
    return currentLang;
  }

  function getAvailableLanguages() {
    return Object.keys(LANGS);
  }

  // Expose globally
  window.i18n = { t, setLanguage, getLanguage, getAvailableLanguages, LANGS };
